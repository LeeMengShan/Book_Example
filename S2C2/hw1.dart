// 回傳「字串」的方法，函數名為iAmStringFunction
// 帶有字串型別的函數參數 iAmString
String iAmStringFunction(String iAmString) {

  // 回傳字串為 雲育鏈+該參數的內容
  String result = "雲育鏈" + iAmString;

  return result;

}

// 調度函數
void main() {
  print(iAmStringFunction('great'));
}
